    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String input = jTextField1.getText();
        Product product = VipSystem.searchProductByName(input);
        DefaultListModel m1 = (DefaultListModel) jList1.getModel();
        m1.clear();
        m1.addElement(String.format("%-10s%-15s%-15s",
                "name", "price", "description"));
        if (product == null) {
            JOptionPane.showMessageDialog(null, "this product name not found");
        } else {
            m1.addElement(product.toStringVipVersion());
            if (product.getType().equals("Tea")) {
                jRadioButton1.setSelected(true);
            } else if (product.getType().equals("Coffee")) {
                jRadioButton2.setSelected(true);
            } else if (product.getType().equals("Slush")) {
                jRadioButton3.setSelected(true);
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed

