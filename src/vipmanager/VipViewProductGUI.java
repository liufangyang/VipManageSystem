    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String o = (String) jList1.getSelectedValue();
        String[] getName = o.split(" ");
        Product product = VipSystem.searchProductByName((getName[0]).trim());
        LinkedList<ProductEvaluationRecord> productEvaluationRecordList = VipSystem.getProductEvaluationRecordList();
        if (productEvaluationRecordList.size() == 0) {
            ProductEvaluationRecord per = new ProductEvaluationRecord(product);
            VipSystem.addToProductEvaluationRecordList(per);
        } else {
            for (int i = 0; i < productEvaluationRecordList.size(); i++) {
                if (productEvaluationRecordList.get(i).getProduct().getProductId() != product.getProductId()) {
                    ProductEvaluationRecord per = new ProductEvaluationRecord(product);
                    VipSystem.addToProductEvaluationRecordList(per);
                    break;
                }
            }
        }
        VipViewProductCommentGUI vvp = new VipViewProductCommentGUI(vip, product);
        vvp.setVisible(true);
        this.dispose();// TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

